def plot(fraction, acc, std, n_pool):


        f, ax = plt.subplots(1,figsize=(7,6))
        labels = ['Core-set with optimization', 'Core-set without optimization', 'K-means++', 'K-means']
        #labels = ['all', 'badge', 'ciral', 'softmax']
        #labels = ['coreset', 'dfal', 'ciral', 'random']
        colors = ['tab:green', 'tab:red', 'tab:blue', 'tab:olive']
        marker = ['x','o','v','^']

        fraction = [round(100*elem/n_pool,1) for elem in fraction]
        for acc, std, label, color, marker in zip(acc, std, labels, colors, marker):
                acc = [100*elem for elem in acc]
                std = [100*elem for elem in std]

                ax.plot(fraction, acc, label=label.upper(), color=color, linewidth=2, marker=marker)
                diff1 = [acc-std for (acc,std) in zip(acc,std)]
                diff2 = [acc+std for (acc,std) in zip(acc,std)]
                ax.fill_between(fraction, diff1, diff2,
                        alpha=0.2)
        

        ax.set_xlim(10, 90)
        ax.set_ylim(10,55)
        ax.set_xlabel("Number labeled samples[%]")
        ax.set_ylabel("Balanced accuracy[%]")
        ax.set_facecolor('aliceblue')
        ax.spines['top'].set_visible(False)
        ax.spines['right'].set_visible(False)
        ax.spines['left'].set_visible(False)
        ax.spines['bottom'].set_visible(False)
        ax.legend(loc=4, framealpha=0)
        plt.xticks(ticks=fraction)

        plt.setp(ax.get_xticklabels()[::2], visible=False)
        plt.grid(color='white')

        plt.show() 


