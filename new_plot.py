def single_plot(n_pool, fraction, acc, std, all_data=None, acc_wo_aug=None, std_wo_aug=None):

        f, ax = plt.subplots(1,figsize=(7,6))
        #labels = ['Core-set with optimization', 'Core-set without optimization', 'K-means++', 'K-means']
        #labels = ['all', 'badge', 'ciral', 'softmax']
        labels = ['coreset', 'dfal', 'ciral', 'random']
        colors = ['tab:green', 'tab:red', 'tab:blue', 'tab:olive']
        linestyle = ['solid', 'dashed', 'dashed', 'solid']
        


        
        if acc_wo_aug is not None:

                fraction = [round(100*elem/n_pool,1) for elem in fraction]
                for acc, std, acc_wo_aug, std_wo_aug, label, color in zip(acc, std, acc_wo_aug, std_wo_aug, labels, colors):
                        acc = [100*elem for elem in acc]
                        std = [100*elem for elem in std]
                        acc_wo_aug = [100*elem for elem in acc_wo_aug]
                        std_wo_aug = [100*elem for elem in std_wo_aug]
                        ax.plot(fraction, acc, label=label.upper(), color=color, linewidth=2)
                        ax.plot(fraction, acc_wo_aug, label=f"{label.upper()}-wo-aug", linestyle='dashed', color=color, linewidth=2)
